<h2>서비스 이용약관</h2>
<p class="contents">본 약관은 “헤이 캐스팅”서비스의 이용자 또는 회원님들께 원활한 시스템을 제공하기 위한 운영 정책으로 관련 제반 서비스의 이용과 이에 필요한 사항들을 규정합니다.</p>

<h3>이용약관</h3>

<div>
    <p class="sub_title">제 1조 약관의 목적</p>
    <p class="contents">본 약관은 메디플랫폼(이하 “회사”)가 운영하는 “헤이 캐스팅앱” 엔터테이너 이력과 정보 제공 및 섭외 직거래 서비스 (이하 “플렛폼”)를 제공함에 있어 이를 이용하는 회원의 이용조건, 절차 및 기타 필요한 사항 등을 규정함을 목적으로 한다.</p>
</div>
<div>
    <p class="sub_title">제 2조 용어의 정의</p>
    <ul>
        <li>1. 이용자: 회사 플랫폼에 접속하여 본 약관에 따라 서비스를 이용하거나 받는 회원 및 비회원을 지칭한다.</li>
        <li>2. 회원: 회사에 개인정보를 제공하고 회원등록을 한 자로서 서비스를 계속적으로 이용할 수 있는 다음 각 호의 사람을 지칭한다. 본인인증 절차를 이용한 사람 또한 회원가입으로 간주한다.
            <ul>
                <li>- 일반 회원: 회사 플랫폼을 이용하기 위하여 일반인 회원 약관에 동의하고 회원가입을 완료하여 이용자 ID를 부여 받은 자로써 주로 기업, 관공서, 단체의 담당자 및 일반인을 말한다.</li>
                <li>- 엔터테이너 회원: 플랫폼을 이용하기 위하여 약관에 동의하고 회원가입을 완료하여 이용자 ID를 부여 받은 자로써 주로 기업 및 관공서, 단체 및 기타 행사에서 행사활동을 수행하는 자를 말한다.</li>
            </ul>
        </li>
        <li>3. 회원ID: 회사의 식별 및 서비스 이용을 위하여 회원이 정한 문자 또는 숫자의 조합을 의미합니다.</li>
        <li>4. 비밀번호: 회원이 부여 받은 회원ID와 일치된 회원임을 확인하고 자신의 비밀 보호를 위해 회원이 설정한 문자 또는 숫자의 조합된 형태이다.</li>
        <li>5. 비회원: 플랫폼을 이용하지 아니한 단순 이용자를 지칭한다.</li>
        <li>6. 서비스: 회사가 운영하는 플랫폼을 통해 일반회원 및 회사가 엔터테이너의 프로필, 행사 요청 사항, 회원 홍보 등의 목적으로 등록하는 자료를 데이터베이스로 저장하고 각각의 회원 요구의 목적에 맞게 가공, 분류하여 정보를 제공하며 최종적으로 일반인 회원과 엔터테이너 회원이 직접적으로 상호행사 계약을 체결하고, 금전(결재)이 오가는데 필요로 하는 모든 부대 서비스를 포함한다.</li>
        <li>7. 충전: 회사가 정한 결제수단 중 엔터테이너 회원이 선택한 결제 수단으로 회사가 지정한 건별 과금 비용을 지불하기 위한 비용을 충전하는 것.</li>
        <li>8. 환불: 회사에 충전한 내용을 현금으로 되돌려 받는 것을 말합니다.</li>
    </ul>
</div>
<div>
    <p class="sub_title">제 3조 약관의 명시와 개정</p>
    <ul>
        <li>1. 회사는 이 약관의 내용과 상호, 영업소 소재지, 대표자의 성명, 사업자등록번호, 연락처 등을 이용자가 알 수 있도록 초기 화면에 게시하거나 기타의 방법으로 이용자에게 공지해야 한다.</li>
        <li>2. 회사는 약관의 규제 등에 관한 법률, 전기통신기본법, 전기통신사업법, 정보통신망이용촉진 등에 관한 법률 등 관련법을 위배하지 않는 범위에서 이 약관을 개정할 수 있다.</li>
        <li>3. 회사가 약관을 개정할 경우에는 적용일자 및 개정사유를 명시하여 현행 약관과 함께 그 개정 약관의 적용일자 7일 전부터 적용일자 전일까지 공지한다. 단 회원의 권리, 의무에 중대한 영향을 주는 변경의 경우에는 적용일자 15일 전부터 공지한다.</li>
        <li>4. 회원은 변경된 약관에 대해 거부할 권리가 있다. 회원은 변경된 약관이 공지된 15일이내에 거부의사를 표명할 수 있다. 회원이 거부하는 경우 본 서비스 제공자인 회사는 15일의 기간을 정하여 “이용자” 에게 사전 통지 후 당해 회원과의 계약을 해지 할 수 있다. 만약, 회원이 거부의사를 표시하지 않거나, 전항에 따라 시행일 이후에 “서비스”를 이용하는 경우에는 동의한 것으로 간주한다.</li>
    </ul>
</div>
<div>
    <p class="sub_title">제 4조 약관 외 준칙</p>
    <p class="contents">서비스 이용에 관하여 본 약관을 적용하며, 본 약관에 명시되지 않은 사항에 대하여는 전기통신기본법, 전기통신사업법 및 기타 관계 법령의 규정에 의한다.</p>
</div>
<div>
    <p class="sub_title">제 5조 이용계약의 성립</p>
    <ul>
        <li>1. 회사의 플랫폼 서비스 이용계약(이하’약관’ 이라 한다)은 서비스를 이용하고자 하는 자의 본 약관과 개인정보취급방침의 내용에 대한 동의 및 이용신청(회원가입신청)을 함으로서 성립된다.</li>
        <li>2. 서비스를 이용하고자 하는 자가 이용신청 당시 인터넷 상의 “동의”를 체크하면 본 약관 및 개인정보 취급방침에 대하여 동의한 것으로 간주한다.</li>
    </ul>
</div>
<div>
    <p class="sub_title">제 6조 플랫폼 서비스 이용신청 및 수집사항</p>
    <p class="contents">회원으로 가입하여 플랫폼을 이용하기를 희망하는 자는 소정의 가입페이지에서 요구하는 사항을 기록하여 신청한다.</p>
    <ul>
        <p>본 플랫폼은 회원에 대한 각종 편의 제공 서비스 제공을 위해 필요한 최소한의 정보를 수집한다. 다음 사항을 필수사항으로 하며 그 외 사항은 선택사항으로 한다.</p>
        <li>1. 	성명, 아이디, 비밀번호
            <p>-목적: 회원제 서비스제공 및 이용자 식별을 위한 목적</p>
        </li>
        <li>2. 	엔터테이너 회원에 대한 휴대폰 연락처, 이메일 주소, 사진, 경력, 소속, 학력사항, 이력사항, 특이사항 주소 등
            <p>-목적: 행사정보 및 프로필 등록과 엔터테이너 지원활동을 위한 목적 및 일반회원과의 연결 및 공유함을 지원하기 위한 목적</p>
        </li>
        <li>3. 	일반회원에 대한 휴대폰 연락처, 이메일 주소, 사진, 주소, 소속회사의 사업자 등록증 등
            <p>-목적: 엔터테이너 회원과의 실시간 대화 및 공유와 행사 요청 정보 등록, 행사에 따른 계산서 발행 등을 위한 목적</p>
        </li>
        <li>4. 	SMS/ 뉴스레터 전송 여부 설정
            <p>-목적: 보다 원활한 행사정보 및 엔터테이너 정보 서비스 제공을 위한 목적 그 외 회원의 기본적 인권 침해의 우려가 있는 민감한 개인정보(인종 및 민족, 사상 및 신조, 출신지 및 본적지, 정치적 성향 및 범죄기록, 건강상태 및 성생활 등)는 수집하지 않는다.</p>
        </li>
    </ul>
</div>
<div>
    <p class="sub_title">제 7조 이용신청의 승낙과 제한</p>
    <p class="contents">회사는 아래 사항에 해당하는 경우에 대해서는 이용신청을 거부할 수 있다.</p>
    <ul>
        <li>1. 사용하고자 하는 회원 ID, 회사명이 미풍양속을 저해할 수 있다고 판단될 경우</li>
        <li>2. 	실제 회사(이름)명과 일치하지 않거나 단체명이 없는 정보를 이용하여 신청한 경우</li>
        <li>3. 	실제 성명이 아니거나 타인의 정보를 이용하여 신청한 경우</li>
        <li>4. 	회원가입 내용을 허위로 기재한 경우</li>
        <li>5. 	회사는 회원의 회사(일반회원)명과 사업자등록번호 허위 여부를 판단하기 위해 사업자등록증을 송부를 요구 할 수 있으며, (개인경우 성명)이에 응하지 않은 경우 회사는 임의로 회원의 ID를 삭제할 수 있다.</li>
    </ul>
</div>
<div>
    <p class="sub_title">제 8조 플랫폼 서비스의 내용</p>
    <p class="contents">회사는 플랫폼을 통해 아래와 같은 서비스를 제공할 수 있으며 그 내용은 다음 각 항과 같다.</p>
    <ul>
        <li>1. 	엔터테이너 회원 프로필 정보를 일반회원에게 제공</li>
        <li>2. 	엔터테이너 회원의 행사 정보를 일반회원에게 제공</li>
        <li>3. 	엔터테이너 회원에게 일반회원의 행사 정보 제공</li>
        <li>4. 	기타 일반회원 및 엔터테이너 회원 요청에 따라 회사의 판단으로 행하는 부가 서비스 회사는 필요한 경우 서비스의 내용을 추가 또는 변경할 수 있다. 단, 이 경우 회사는 추가 또는 변경내용을 회원에게 공지한다.</li>
    </ul>
</div>
<div>
    <p class="sub_title">제 9조 엔터테이너 회원 프로필 정보, 행사 정보 등을 일반회원에게 노출</p>
    <p class="contents">제 9조는 엔터테이너 회원에 해당하는 사항이다.</p>
    <ul>
        <li>1. 	프로필 작성 및 수정한 내용대로 플랫폼에서 일반회원에게 노출된다.</li>
        <li>2. 	엔터테이너 회원이 작성한 주요 행사 제목, 내용, 시간, 시간당 행사비 등을 텍스트 및 이미지 형태로 작성, 수정할 수 있으며 플랫폼에서 일반회원에게 노출된다.</li>
        <li>3. 	전항의 행사비는 부가가치세를 미포함, 교통비 포함 가격이며 플랫폼은 일반회원이 이를 인지하도록 노출한다.</li>
        <li>4. 	엔터테이너 회원의 연락처는 노출되지 않는 것을 원칙으로 한다.</li>
        <li>5. 	회사는 안정적인 서비스를 제공하기 위해 테스트 및 모니터링 용도로 HEY CASTING(헤이 캐스팅) 운영자가 엔터테이너 회원의 이력서 정보를 열람 할 수 있다.</li>
    </ul>
</div>
<div>
    <p class="sub_title">제 10조 일반회원의 엔터테이너 정보, 프로필, 행사 정보 노출</p>
    <p class="contents">일반회원은 필요로 하는 행사를 요청할 경우 엔터테이너 회원의 판단을 돕기 위해 행사정보 및 행사 니즈를 정확히 기재하여야 한다. 일반회원은 허위정보를 기재하여 회사와 엔터테이너 회원에 피해를 미치지 않아야 할 기본의무가 있다.</p>
    <ul>
        <p class="label">허위정보의 종류</p>
        <li>1. 행사요청정보를 가장하여 본 서비스와 상관없는 용역 정보를 게시하는 경우(예, 직업알선, 상품판매. 부업알선, 금융모집 등)</li>
        <li>2. 	일반회원의 업체명 또는 성명, 연락처, 행사관련 내용이 사실과 다를 경우</li>
        <li>3. 	엔터테이너 회원을 섭외하고도 반복적으로 섭외를 일방적으로 취소하는 경우</li>
        <li>4. 	일반회원이 제시한 행사조건(시간, 행사비)등이 제안 요청할 때의 그것과 현저히 다른 경우</li>
        <li>5. 	기타 행사관련 주요내용이 사실과 다른 정보를 제공하여 엔터테이너 회원에게 피해를 끼치는 경우</li>
    </ul>
    <p class="contents">위반한 경우 이에 대한 책임은 일반회원에게 있다. 회사는 일반회원이 등록 및 요청한 정보 및 각종 내용을 회사가 정한 방법에 의해 노출할 수 있으며, 회사가 지정한 기간이 지난 정보 및 내용은 회사가 임의로 마감시킬 수 있다. 일반회원은 무료로 엔터테이너 회원이 등록한 프로필을 검색할 수 있다.</p>
</div>
<div>
    <p class="sub_title">제 11조 제휴를 통한 서비스</p>
    <ul>
        <li>1. 회사는 제휴 관계를 체결한 뒤 인터넷 웹사이트 또는 신문, 잡지 등의 오프라인 매체를 통해 사이트에 등록 또는 유선으로 요청한 일반회원에게 행사정보가 열람될 수 있도록 서비스를 제공할 수 있다.</li>
        <li>2. 	회사는 제휴를 통해 타 사이트 및 매체에 등록될 수 있음을 고지해야 하며, 제휴 사이트 전체 목록을 사이트 내에서 상시 열람 할 수 있도록 해야 한다.</li>
    </ul>
</div>
<div>
    <p class="sub_title">제 12조 행사 중개에 따른 이용료에 관한 사항</p>
    <ul>
